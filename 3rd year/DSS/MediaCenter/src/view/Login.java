    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String e = email.getText();
        String pwd = String.valueOf(pass.getPassword());

        try {
            PrintWriter pw = new PrintWriter(this.socket.getOutputStream());
            pw.println("iniciaSessao?" + e + "?" + pwd + "?" + this.tipo);
            pw.flush();
            BufferedReader br = new BufferedReader(new InputStreamReader(this.socket.getInputStream()));
            String res = br.readLine();
            if (!res.equals("Sucesso")) {
                JOptionPane.showMessageDialog(this, res);
                return;
            }
        } catch (IOException m) {
            JOptionPane.showMessageDialog(this, "Erro de conex√£o");
            return;
        }
        if (this.tipo.equals("User"))
            new Inicio(this.socket).run();
        else new Inicio_Admin(this.socket).run();
        this.setVisible(false);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

