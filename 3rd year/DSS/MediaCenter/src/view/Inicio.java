    private void terminarSessao(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            PrintWriter pw = new PrintWriter(this.socket.getOutputStream());
            pw.println("terminarSessao");
            pw.flush();
            BufferedReader br = new BufferedReader(new InputStreamReader(this.socket.getInputStream()));
            String res = br.readLine();
            if (!res.equals("Sucesso")){
                JOptionPane.showMessageDialog(this, "Ocorreu um erro a interpretar a mensagem");
                return;
            }
        }
        catch (IOException e){
            JOptionPane.showMessageDialog(this, "Erro de conex√£o");
            return;
        }
        new Menu_Inicial(this.socket).run();
        this.setVisible(false);
        dispose();
    }//GEN-LAST:event_jButton4ActionPerformed

    private void upload(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File f = chooser.getSelectedFile();

        String ff = f.getAbsolutePath();
        String ext = ff.substring(ff.lastIndexOf("."));
        if (ext.equals(".mp3")){
            new Inserir_nomeMusica(ff,this.socket).run();
        }
        else new Inserir_nomeVideo(ff,this.socket).run();
        this.setVisible(false);
        dispose();
    }//GEN-LAST:event_jButton3ActionPerformed


