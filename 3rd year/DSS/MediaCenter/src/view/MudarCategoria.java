    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String novogen = Objects.requireNonNull(jComboBox1.getSelectedItem()).toString();
        if (novogen.equals("<Selecione Categoria>")){
            JOptionPane.showMessageDialog(this, "Genero invalido");
        }
        else {
            String fich = this.nome + " - " + this.artista + ".mp3";
            try {
                PrintWriter pw = new PrintWriter(this.socket.getOutputStream());
                pw.println("alteraCategoria?" + fich + "?" +novogen);
                pw.flush();
                BufferedReader br = new BufferedReader(new InputStreamReader(this.socket.getInputStream()));
                String res = br.readLine();
                if (!res.equals("Sucesso")){
                    JOptionPane.showMessageDialog(this, res);
                    return;
                }
            }
            catch (IOException e){
                JOptionPane.showMessageDialog(this, "Erro de conex√£o");
                return;
            }
            new Reproduzir_Musica(this.socket, this.nome, this.artista,this.tabela).run();
            this.setVisible(false);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

